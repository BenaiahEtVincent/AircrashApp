                            l’Atlantique en quittant les côtes brésiliennes. À 2h10, heure locale, les sondes Pitot
                            gèlent
                            ce qui fait sauter le pilotage automatique. Le commandant de bord est parti se coucher
                            depuis
                            quelques minutes et le jeune copilote a pris sa place. Sous l’effet de la peur, ne
                            comprenant
                            pas pourquoi la vitesse diminue, il tire sur le manche. L’avion monte jusqu’à 12’000 mètres
                            d’altitude. L’avion décroche et l’alarme d'urgence se déclenche. Dans le cockpit plus
                            personne
                            ne comprend ce qu'il est en train de se passer. Le commandant de bord est réveillé et ne
                            comprend rien non plus. Le jeune copilote continue à tirer sur le manche sans que les autres
                            membres ne le remarque. L’avion chute alors sans aucune retenue pendant plus de 3 minutes et
                            s’écrase dans l’océan.
                        </p>
